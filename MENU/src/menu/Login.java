    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        user = jTextUser.getText();
        char[] arrayC = jPassword.getPassword();
        password = new String(arrayC);
        
        int index = 0;
        
        for ( String usuario : users)
        {
            
            if ( usuario.equals(user))
            {
                flagUser = true;
            } 
            
            if ( flagUser == true)
            {
                if ( passwords[index].equals(password))
                    flagPassword = true;
                    index = 0;
                    break;
            }
            
            index++;
        }
        
        if ( flagPassword == true)
        {
            ChooseGame chooseGame = new ChooseGame();
            chooseGame.setVisible(true);
            this.setVisible(false);
        }
        
        flagUser = false;
        flagPassword = false;
        
    }//GEN-LAST:event_jButton1ActionPerformed

